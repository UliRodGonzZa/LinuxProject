    # Obtener hora
    if [ -f /proc/driver/rtc ]; then
         # Si existe el archivo /proc/driver/rtc (controlador del reloj de tiempo real, RTC):
        fecha=$(grep "rtc_date" /proc/driver/rtc | sed 's/rtc_date[ \t]*:[ \t]*//')
        # Extrae la fecha (rtc_date) eliminando etiquetas y espacios innecesarios.
        hora=$(grep "rtc_time" /proc/driver/rtc | sed 's/rtc_time[ \t]*:[ \t]*//')

        IFS='-' read -r anio mes dia <<< "$fecha"
          # Divide la fecha obtenida en año, mes y día usando el carácter '-' como separador.
        IFS=':' read -r h m s <<< "$hora"
        # Corrige la hora del sistema para ajustarla al huso horario de la CDMX
        # restando 6 horas. Se usa (10#$h) para tratar la hora como decimal puro.
        h_corrigida=$(( (10#$h - 6 + 24) % 24 ))
        hora_mexico=$(printf "%02d:%02d:%02d" "$h_corrigida" "$m" "$s")
    else
           # Si no existe el archivo RTC utilizamos comando basico =>
        fecha_hora=$(date "+%d/%m/%Y %H:%M:%S")
        hora_mexico="$fecha_hora"
        h_corrigida=$(date "+%H")
    fi

    hora_sistema=$(date "+%H:%M:%S")

    echo ""
